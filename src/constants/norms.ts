export interface TScoreResult {
  value: number | null;
  direction?: 'T' | 'F' | '';
}

export const getTScore = (scaleName: string, rawScore: number): TScoreResult => {
  const norm = MMPI3_NORMS[scaleName];
  if (!norm) return { value: null };
  
  const tValue = norm[rawScore];
  if (tValue === undefined) {
    if (rawScore < 0) return { value: norm[0] ?? null };
    return { value: norm[norm.length - 1] ?? null };
  }

  if (scaleName === 'TRIN') {
    if (rawScore === 0) return { value: null };
    if (rawScore > 13) return { value: tValue, direction: 'T' };
    if (rawScore < 13) return { value: tValue, direction: 'F' };
    return { value: tValue, direction: '' };
  }

  return { value: tValue };
};

export const formatTScore = (result: TScoreResult): string => {
  if (result.value === null) return '—';
  return `${result.value}${result.direction || ''}`;
};

export const getTNumeric = (result: TScoreResult): number | null => {
  return result.value;
};

